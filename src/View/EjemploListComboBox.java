    private void filtroComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filtroComboBoxActionPerformed
        marca = (String) filtroComboBox.getSelectedItem();
        if ("Ford".equals(marca)) {
        // L贸gica para mostrar modelos de Ford en listaFiltrada
        List<String> modelosFord = Arrays.asList("Ranger 2020", "Ranger 2022", "Escape 2020", "Escape 2021", "Fiesta 2022", "Ecosport 2022", "Explorer 2020", "Explorer 2021", "Explorer 2022");
        listaFiltrada.setListData(modelosFord.toArray(new String[0]));
        } else if ("Toyota".equals(marca)) {
        // L贸gica para mostrar modelos de Toyota en listaFiltrada
        List<String> modelosToyota = Arrays.asList("Corolla 2020", "Corolla 2021", "Corolla 2022", "Fortuner 2021", "Fortuner 2022", "Prado 2021", "Prado 2022");
        listaFiltrada.setListData(modelosToyota.toArray(new String[0]));
        } else if ("Nissan".equals(marca)) {
        // L贸gica para mostrar modelos de Nissan en listaFiltrada
        List<String> modelosNissan = Arrays.asList("Sentra 2020", "Sentra 2021", "March 2022", "Versa 2021", "Versa 2022", "X-trail 2021", "X-trail 2022", "Frontier 2020");
        listaFiltrada.setListData(modelosNissan.toArray(new String[0]));
        } else if ("Mazda".equals(marca)) {
        // L贸gica para mostrar modelos de Mazda en listaFiltrada
        List<String> modelosMazda = Arrays.asList("BT50 2020", "BT50 2021", "BT50 2022", "CX-3 2021", "CX-3 2022", "CX-5 2021", "CX-5 2022");
        listaFiltrada.setListData(modelosMazda.toArray(new String[0]));
        } else {
        listaFiltrada.setListData(new String[0]); // Limpiar la lista
        }
    }//GEN-LAST:event_filtroComboBoxActionPerformed

    private void listaFiltradaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaFiltradaValueChanged
         listaFiltrada.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    // Obtener el nombre del modelo seleccionado
                    String marcaSeleccionada = (String) filtroComboBox.getSelectedItem();
                    String modeloSeleccionado = listaFiltrada.getSelectedValue();
                    
                    // JLabel con salida de modelo y marca seleccionada
                    jLabel4.setText("Ha selecionado " + marcaSeleccionada + " " + modeloSeleccionado);
                }
            }
        });
    }//GEN-LAST:event_listaFiltradaValueChanged

